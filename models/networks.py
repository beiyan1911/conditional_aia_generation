class AttCNNNet(nn.Module):
    def __init__(self, ntf=64, ncf=64, out_size=49, trans_vec_size=6, n_layers=10):
        super(AttCNNNet, self).__init__()

        # 输入数据为 N, D, H, W
        self.siamese = SiameseNet(snf=ntf, out_size=trans_vec_size)
        self.transformer = TransformerNet(out_size, trans_vec_size ** 2)

        # 输入数据为 N, C, D, H, W ,  batch x 1 x 49 x 256 x 256
        k_size = 3
        padding = 1
        layers = []

        layers += [nn.Conv2d(out_size, ncf, k_size, padding=padding), Mish()]
        for _ in range(n_layers - 2):
            layers += [nn.Conv2d(ncf, ncf, k_size, padding=padding), nn.InstanceNorm2d(ncf), Mish()]

        layers += [nn.Conv2d(ncf, 1, k_size, padding=padding)]
        self.cnn = nn.Sequential(*layers)

    def forward(self, x):
        # x [N,100,w,h]
        features = self.siamese(x).transpose(0, 1)  # [100,N,36]
        trans_vec = self.transformer(features)  # [N,49,100]

        in_size = x.size()
        new_d = trans_vec.size(1)

        batch, d, w, h = in_size[0], in_size[1], in_size[2], in_size[3]
        output = torch.bmm(trans_vec, x.view(batch, d, -1))
        output = output.view(batch, new_d, w, h)
        out = self.cnn(output)
        return out


