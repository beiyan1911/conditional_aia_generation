class SiameseNet(nn.Module):
    """
    输入：N，D，W，H
    输出：N，D，out_size**2
    """

    def __init__(self, snf=32, out_size=6):
        """
        输入:N,D,W,H
        输出:N,D,Feature
        """
        super(SiameseNet, self).__init__()

        self.convnet = nn.Sequential(
            nn.Conv2d(1, snf, kernel_size=11, stride=2),  # 123
            nn.MaxPool2d(kernel_size=3, stride=2),  # 61
            nn.PReLU(),
            nn.Conv2d(snf, snf * 2, kernel_size=5),  # 57
            nn.MaxPool2d(kernel_size=3, stride=2),  # 28
            nn.PReLU(),
            nn.Conv2d(snf * 2, snf * 2, kernel_size=3),  # 26
            nn.PReLU(),
            nn.Conv2d(snf * 2, snf * 2, kernel_size=3),  # 22
            nn.PReLU(),
            nn.Conv2d(snf * 2, snf, kernel_size=3),  # 20
            nn.PReLU(),
            nn.Conv2d(snf, 1, kernel_size=3),  # 18
            nn.AdaptiveMaxPool2d(out_size)
        )

        # self.fc = nn.Sequential(nn.Linear(snf * out_size * out_size, snf),
        #                         nn.PReLU(),
        #                         nn.Linear(snf, out_size * out_size)
        #                         )

