class DoubleConv(nn.Module):
    def __init__(self, in_dims, out_dims, kernel_size=3, stride=1, padding=1, use_bias=False, norm=False,
                 norm_layer=nn.InstanceNorm2d):
        super().__init__()

        layers = [nn.ReflectionPad2d(padding),
                  nn.Conv2d(in_dims, out_dims, kernel_size, stride, bias=use_bias)]

        if norm:
            layers += [norm_layer(out_dims)]

        layers += [
            nn.ReLU(True),
            nn.ReflectionPad2d(padding),
            nn.Conv2d(out_dims, out_dims, kernel_size, stride, bias=use_bias)]

        if norm:
            layers += [norm_layer(out_dims)]

        layers += [nn.ReLU(True)]

        self.model = nn.Sequential(*layers)

    def forward(self, x):
        return self.model(x)


class ResnetBlock(nn.Module):
    def __init__(self, dim, kernel_size=3, stride=1, padding=1, use_bias=False, norm=True,
                 norm_layer=nn.InstanceNorm2d):
        super(ResnetBlock, self).__init__()
        conv_block = []
        # conv
        conv_block += [nn.ReflectionPad2d(padding),
                       nn.Conv2d(dim, dim, kernel_size, stride, padding=0, bias=use_bias)]
        # norm
        if norm:
            conv_block += [norm_layer(dim)]

        # act
        conv_block += [nn.ReLU(True)]

        # conv
        conv_block += [nn.ReflectionPad2d(padding),
                       nn.Conv2d(dim, dim, kernel_size, stride, padding=0, bias=use_bias)]

        # norm
        if norm:
            conv_block += [norm_layer(dim)]

        self.conv_block = nn.Sequential(*conv_block)

    def forward(self, x):
        return x + self.conv_block(x)


class MultiResnet(nn.Module):

    def __init__(self, in_dims=5, out_dims=1, n_res=5, nf=32, norm_type='instance'):
        super(MultiResnet, self).__init__()
        norm_layer = utils.get_norm_layer(norm_type=norm_type)
        self.x3_conv = DoubleConv(in_dims, nf, kernel_size=3, stride=1, padding=1, use_bias=False,
                                  norm_layer=norm_layer)
        self.x5_conv = DoubleConv(in_dims, nf, kernel_size=5, stride=1, padding=2, use_bias=False,
                                  norm_layer=norm_layer)
        self.x7_conv = DoubleConv(in_dims, nf, kernel_size=7, stride=1, padding=3, use_bias=False,
                                  norm_layer=norm_layer)
        features = []
        for i in range(n_res):
            features += [ResnetBlock(nf, kernel_size=3, stride=1, padding=1, use_bias=False, norm_layer=norm_layer)]
        self.features = nn.Sequential(*features)

        self.final_conv = nn.Conv2d(nf * 3, out_dims, 3, 1, padding=1, bias=False)

    def forward(self, x):
        x3_features = self.x3_conv(x)
        x5_features = self.x5_conv(x)
        x7_features = self.x7_conv(x)
        out = self.features(x3_features)
        out = torch.cat([out, x5_features, x7_features], dim=1)
        out = self.final_conv(out)
        return out


