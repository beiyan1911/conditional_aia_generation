class UNet(nn.Module):
    def __init__(self, in_dim, out_dim, bilinear=True):
        super(UNet, self).__init__()
        self.in_dim = in_dim
        self.out_dim = out_dim
        self.bilinear = bilinear

        self.inc = DoubleConv(in_dim, 64)
        self.down1 = Down(64, 128)
        self.down2 = Down(128, 256)
        self.down3 = Down(256, 512)
        self.down4 = Down(512, 512)
        self.up1 = Up(1024, 256, bilinear)
        self.up2 = Up(512, 128, bilinear)
        self.up3 = Up(256, 64, bilinear)
        self.up4 = Up(128, 64, bilinear)
        self.outc = OutConv(64, out_dim)
        self.tanh = nn.Tanh()

    def forward(self, x):
        x1 = self.inc(x)
        x2 = self.down1(x1)
        x3 = self.down2(x2)
        x4 = self.down3(x3)
        x5 = self.down4(x4)
        x = self.up1(x5, x4)
        x = self.up2(x, x3)
        x = self.up3(x, x2)
        x = self.up4(x, x1)
        out = self.outc(x)
        out = self.tanh(out)
        return out


class DnCNN(nn.Module):
    def __init__(self, in_dims, nf, num_of_layers=17):
        super(DnCNN, self).__init__()
        kernel_size = 3
        padding = 1
        features = nf
        layers = []
        layers.append(nn.Conv2d(in_dims, features, kernel_size, padding=padding, bias=False))
        layers.append(nn.ReLU(inplace=True))
        # layers.append(Mish())
        for _ in range(num_of_layers - 2):
            layers.append(nn.Conv2d(features, features, kernel_size, padding=padding, bias=False))
            # layers.append(nn.BatchNorm2d(features))
            layers.append(nn.InstanceNorm2d(features))
            layers.append(nn.ReLU(inplace=True))
            # layers.append(Mish())
        layers.append(nn.Conv2d(features, 1, kernel_size, padding=padding, bias=False))
        self.dncnn = nn.Sequential(*layers)

    def forward(self, x):
        out = self.dncnn(x)
        return out


